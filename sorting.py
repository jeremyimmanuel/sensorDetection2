def refactor(arr):
    start = float(arr[0][0]) #get earliest time
    curr = 1
    newDict = {}

    for p in arr:
        if float(p[0]) < start + curr:
            if(curr in newDict.keys()):
                newDict[curr] += int(p[1])
            else:
                newDict[curr] = int(p[1])
        else:
            start += 1
            curr += 1
    
    print(newDict)

